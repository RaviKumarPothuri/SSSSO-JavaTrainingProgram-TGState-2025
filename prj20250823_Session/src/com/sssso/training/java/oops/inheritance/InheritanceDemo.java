package com.sssso.training.java.oops.inheritance;

public class InheritanceDemo {

	public static void main(String[] args) {
		
//		Inheritance: It is the mechanism that allows a class to acquire behavior of another class.
//		IS-A relationship
//		The class that serves the behavior to another class is called as Parent or Super class.
//		The class that acquires the behavior from another class is called as Child class.
		
//		Single Inheritance, Multilevel Inheritance
		
		ChildClass c1 = new ChildClass();
		c1.addValues(10, 20);
		c1.addValues(10, 20, 30);
		
	}
	
	


}
